public class question38 {
    public static void main(String[] args) {
        int[] arr = {3,6,7,1,5,8};
        int sum = 0;
        for (int e : arr) {
            sum += e;
            System.out.print(e + " ");
        }
        System.out.println(sum);
    }
}
